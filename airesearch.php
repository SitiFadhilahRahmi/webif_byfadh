                                    <div class="textblock">
                                        <p>Departemen Informatika Universitas Andalas terus menunjukkan komitmennya dalam mendukung kemajuan ilmu pengetahuan dan teknologi melalui berbagai penelitian inovatif di bidang Artificial Intelligence (AI) dan Machine Learning (ML). Sebagai salah satu pusat akademik terkemuka di Sumatra Barat, departemen ini memanfaatkan teknologi AI untuk menjawab berbagai tantangan lokal dan global, mulai dari pertanian cerdas hingga sistem kesehatan berbasis data.
Salah satu fokus utama penelitian adalah pengembangan algoritma machine learning yang mampu menganalisis data secara efisien dan memberikan prediksi akurat. Dengan pendekatan yang inovatif, penelitian ini bertujuan untuk meningkatkan kualitas hidup masyarakat melalui solusi berbasis teknologi, seperti deteksi dini penyakit, optimasi hasil panen, sistem transportasi pintar, dan analisis sentimen media sosial.
Selain itu, Departemen Informatika Unand secara aktif menjalin kolaborasi dengan berbagai pihak, termasuk lembaga pemerintah, sektor industri, dan institusi pendidikan lainnya. Kolaborasi ini bertujuan untuk memastikan bahwa hasil penelitian tidak hanya bersifat teoritis, tetapi juga aplikatif dan memberikan dampak langsung bagi masyarakat luas.</p>
